function Script69()
{
  var player = GetPlayer();

// Initialize sum
var sum = 0;

// Loop through score variables and sum them
for(var i = 1; i <= 20; i++) {
    var scoreVariableName = "SMPQ" + i + "Score";
    var value = player.GetVar(scoreVariableName);
    console.log(scoreVariableName + ": " + value); // Log the value to the console

    var numericValue = parseFloat(value);
    if (isNaN(numericValue)) {
        numericValue = 0; // Set to 0 if not a number
    }

    sum += numericValue;
}

sum *= 2; // Double the sum

console.log("Total Sum: " + sum); // Log the total sum
player.SetVar("countingScore", sum);


}

